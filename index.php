            <p class="t11Copy12 style4 style5"><a name="1"></a><br>
                        <span class="style77"><span class="style9"><strong>01. &nbsp;What is private gifting?</strong> &nbsp;<a href="#top" class="t9">-TOP-</a></span></span></p>
<p class="t11Copy12 style4 style5 style77">Private gifting is a concept embraced by private  groups of consenting adults as individuals and has existed for many years. This  private activity does not involve network marketing, multi-level marketing, or  a kind of business nor commercial ventures. There are no business transactions,  investments and/or securities involved in this activity. There is no business  or company name or location and there are no directors, officers, shareholders  or principals. Private Individuals simply voluntarily support each other in a  team concept which helps change participant’s lives.</p>
<p class="style8">The concept of  private gifting is based upon the fact that both American and Canadian citizens  have the Constitutional right to gift property, cash and other assets, and are  subject to the rules and regulations established by the laws. The U.S. gifting  rules are found in the IRS Tax Code, Title 26, Sections 2501-2504 and 2511.                      </p>
                      <p class="t11Copy12 style4 style5 style9">The law states that one or more individuals can  give a gift to another individual of up to $13,000 each per calendar year  without any tax liability to the receiver of the gift, because any taxes on the  gift has already been paid. Per the Law GIFTS, Donations, Pledges,  contributions, etc., are not included in the gross income of the recipient.  Also, see United States, IRS Publication 950.</p>
                      <p class="t11Copy12 style4 style5 style9"><a name="2" id="2"></a><br>
                        <strong>02. &nbsp;How did this concept begin?</strong> 
